// Generates a server-side, React-free descriptor-field registry from the frontend
// studio-kit descriptor files. The registry is consumed by DefaultsSettingsValidator
// to validate media default `settings` against the actual provider+operation field schema.

import pkg from 'glob';
const { sync: globSync } = pkg;
import fs from 'node:fs';
import path from 'node:path';
import ts from 'typescript';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const DESCRIPTOR_GLOB = path.join(
  __dirname,
  '../apps/frontend/src/components/media-tools/*/descriptor.ts',
);
const OUT_FILE = path.join(
  __dirname,
  '../libraries/nestjs-libraries/src/ai/defaults/studio-descriptor-fields.generated.ts',
);

const files = globSync(DESCRIPTOR_GLOB).sort();

const registry = {};

for (const file of files) {
  const source = fs.readFileSync(file, 'utf8');

  // Skip descriptors that import runtime values (e.g. React panels). They have no
  // generic form fields and are not used by the studio-kit default path.
  if (/^import\s+\{[^}]+\}\s+from\s+['"][^'"]+['"];?\s*$/m.test(source)) {
    const base = path.basename(path.dirname(file));
    console.log(`Skipping ${base} (runtime import)`);
    continue;
  }

  const result = ts.transpileModule(source, {
    compilerOptions: {
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
      jsx: ts.JsxEmit.Preserve,
      removeComments: true,
    },
    reportDiagnostics: true,
  });

  if (result.diagnostics && result.diagnostics.length > 0) {
    console.error(`Transpile errors in ${file}:`, result.diagnostics);
    continue;
  }

  const exports = {};
  const moduleObj = { exports };
  const code = result.outputText;
  try {
    const fn = new Function(
      'exports',
      'module',
      'require',
      '__dirname',
      '__filename',
      code,
    );
    fn(exports, moduleObj, require, path.dirname(file), file);
  } catch (err) {
    console.error(`Failed to evaluate ${file}:`, err.message);
    continue;
  }

  const descriptor = Object.values(moduleObj.exports).find(
    (v) => v && typeof v === 'object' && v.provider && Array.isArray(v.tabs),
  );

  if (!descriptor) {
    console.error(`No descriptor export found in ${file}`);
    continue;
  }

  const tabs = descriptor.tabs.map((tab) => ({
    key: tab.key,
    operation: tab.operation,
    model: tab.model,
    fields: (tab.fields || []).map((field) => ({
      name: field.name,
      type: field.type,
    })),
  }));

  if (registry[descriptor.provider]) {
    registry[descriptor.provider].tabs.push(...tabs);
    console.log(`Merged ${descriptor.provider} (+${descriptor.tabs.length} tabs)`);
  } else {
    registry[descriptor.provider] = {
      provider: descriptor.provider,
      tabs,
    };
    console.log(`Registered ${descriptor.provider} (${descriptor.tabs.length} tabs)`);
  }
}

const out = `// Auto-generated by scripts/generate-studio-descriptor-registry.mjs.
// Do not edit by hand. Source of truth: apps/frontend/src/components/media-tools/*/descriptor.ts

export interface StudioFieldSchema {
  name: string;
  type: string;
}

export interface StudioTabSchema {
  key: string;
  operation: 'video' | 'image' | 'audio';
  model?: string;
  fields: StudioFieldSchema[];
}

export interface StudioDescriptorSchema {
  provider: string;
  tabs: StudioTabSchema[];
}

export const STUDIO_DESCRIPTOR_REGISTRY: Record<string, StudioDescriptorSchema> = ${JSON.stringify(
  registry,
  null,
  2,
)};
`;

fs.writeFileSync(OUT_FILE, out, 'utf8');
console.log(`\nWrote ${Object.keys(registry).length} descriptors to ${OUT_FILE}`);
